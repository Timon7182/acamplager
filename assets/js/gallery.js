(function renderGallery() {
  function fillGrid(containerId, folder, files, alt) {
    var el = document.getElementById(containerId);
    if (!el) return;
    el.innerHTML = files.map(function (name) {
      var href = folder + "/" + encodeURIComponent(name);
      return '<figure><img src="' + href + '" alt="' + alt + '" loading="lazy" /></figure>';
    }).join("");
  }

  function init() {
    fillGrid("gallery-photos", "photo-material", window.GALLERY.photos, "A CAMP — фото");
    fillGrid("gallery-reviews", "review", window.GALLERY.reviews, "Отзыв родителя");
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }
})();
